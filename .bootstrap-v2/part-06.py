    '    except yt_dlp.utils.DownloadError as exc:\n',
    '        message = str(exc).replace("ERROR:", "").strip()\n',
    '        raise InputError(\n',
    '            f"YouTube download failed: {message}. Update the lockfile or use a local file; some videos require cookies."\n',
    '        ) from exc\n',
    '    duration = float(info.get("duration") or probe_duration(downloaded))\n',
    '    if duration > max_duration_seconds:\n',
    '        raise InputError(f"Video exceeds the {max_duration_seconds / 60:.0f}-minute limit.")\n',
    '    destination = workdir / "source.wav"\n',
    '    normalize_audio(downloaded, destination)\n',
    '    return PreparedAudio(\n',
    '        destination, _safe_title(str(info.get("title") or "YouTube song")), url.strip(), duration\n',
    '    )\n',
