    '\n',
    '\n',
    'class InputError(ValueError):\n',
    '    pass\n',
    '\n',
    '\n',
    'class DependencyError(RuntimeError):\n',
    '    pass\n',
    '\n',
    '\n',
    '@dataclass(slots=True)\n',
    'class PreparedAudio:\n',
    '    path: Path\n',
    '    title: str\n',
    '    source: str\n',
    '    duration: float\n',
    '\n',
    '\n',
    'def is_youtube_url(value: str) -> bool:\n',
    '    try:\n',
    '        parsed = urlparse(value.strip())\n',
