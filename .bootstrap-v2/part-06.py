    '        "-v",\n',
    '        "error",\n',
    '        "-show_entries",\n',
    '        "format=duration",\n',
    '        "-of",\n',
    '        "json",\n',
    '        str(path),\n',
    '    ]\n',
    '    process = subprocess.run(command, capture_output=True, text=True, check=False, timeout=30)\n',
    '    if process.returncode != 0:\n',
    '        raise InputError(process.stderr.strip() or "ffprobe could not read this file")\n',
    '    try:\n',
    '        duration = float(json.loads(process.stdout)["format"]["duration"])\n',
    '    except (KeyError, TypeError, ValueError, json.JSONDecodeError) as exc:\n',
    '        raise InputError("Could not determine media duration.") from exc\n',
    '    if duration <= 0:\n',
    '        raise InputError("The media file has no readable audio duration.")\n',
    '    return duration\n',
    '\n',
    '\n',
    'def normalize_audio(source: str | Path, destination: str | Path) -> Path:\n',
    '    destination = Path(destination)\n',
    '    destination.parent.mkdir(parents=True, exist_ok=True)\n',
    '    command = [\n',
    '        require_binary("ffmpeg"),\n',
    '        "-hide_banner",\n',
    '        "-loglevel",\n',
    '        "error",\n',
    '        "-y",\n',
    '        "-i",\n',
