    '        "fragment_retries": 3,\n',
    '        "extractor_retries": 2,\n',
    '        "match_filter": reject,\n',
    '        "restrictfilenames": True,\n',
    '    }\n',
    '    cookie_file = os.getenv("SGCHORDS_COOKIES_FILE")\n',
    '    if cookie_file:\n',
    '        cookie_path = Path(cookie_file).expanduser()\n',
    '        if not cookie_path.is_file():\n',
    '            raise DependencyError("SGCHORDS_COOKIES_FILE is not readable.")\n',
    '        options["cookiefile"] = str(cookie_path)\n',
